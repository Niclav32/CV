/*Modificador de color de fondo*/
function cambiar_colorfondo() {
    let body = document.getElementsByTagName ("body") [0];
    body.style.backgroundColor = "#f00";
}

/*Restaurador de color de fondo*/
function restaurar_colorfondo() {
    let body = document.getElementsByTagName ("body") [0];
    body.style.backgroundColor = "hsl(212, 21%, 14%)";
}